"""Generate src/twsstarter/resources/help_images.py.

Encodes the help screenshots from res/ as base64 PNG data URIs so the help can
embed them inline — no runtime file dependency and nothing extra to bundle.
Run after (re)generating the screenshots with make_screens.py:

    uv run python build/make_help_images.py
"""
from __future__ import annotations

import base64
import os
import sys
from pathlib import Path

os.environ.setdefault("QT_QPA_PLATFORM", "offscreen")  # QImage only, no widgets

from PyQt6.QtCore import QBuffer, QByteArray, QIODevice, Qt  # noqa: E402
from PyQt6.QtGui import QImage  # noqa: E402
from PyQt6.QtWidgets import QApplication  # noqa: E402

_ROOT = Path(__file__).resolve().parent.parent
_RES = _ROOT / "res"
_OUT = _ROOT / "src" / "twsstarter" / "resources" / "help_images.py"

# Help image key -> source PNG in res/. Keys are referenced from the help text
# as [[IMG:<key>]] placeholders (see i18n/strings.py).
_IMAGES = {
    "main": "ui_main.png",
    "add": "ui_connection_add.png",
    "settings": "ui_settings.png",
}
_MAX_WIDTH = 760  # help column is ~780px; scale down for a smaller data URI


def _data_uri(path: Path) -> str:
    img = QImage(str(path))
    if img.width() > _MAX_WIDTH:
        img = img.scaledToWidth(_MAX_WIDTH, Qt.TransformationMode.SmoothTransformation)
    ba = QByteArray()
    buf = QBuffer(ba)
    buf.open(QIODevice.OpenModeFlag.WriteOnly)
    img.save(buf, "PNG")
    buf.close()
    return "data:image/png;base64," + base64.b64encode(bytes(ba)).decode("ascii")


def main() -> None:
    _app = QApplication.instance() or QApplication(sys.argv)
    lines = [
        '"""Auto-generated by build/make_help_images.py — do not edit by hand.',
        "",
        "Base64 PNG data URIs of the help screenshots, embedded so the help",
        'renders them inline without any runtime file dependency."""',
        "",
        "DATA_URIS = {",
    ]
    for key, filename in _IMAGES.items():
        src = _RES / filename
        if not src.exists():
            print("MISSING", src)
            continue
        lines.append(f"    {key!r}: {_data_uri(src)!r},")
    lines.append("}")
    _OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print("wrote", _OUT.relative_to(_ROOT), f"({_OUT.stat().st_size} bytes)")


if __name__ == "__main__":
    main()
